place=['xiamen','wuhan','beijing','tianjing','chongqing']
print("原始的输出列表是:")
print(place)
print("临时排列为：")
print(sorted(place))
print("临时反向排列为：")
print(sorted(place,reverse=True))
place.reverse()
print("反向排列：")
print(place)
place.reverse()
print("反向排列：")
print(place)
place.sort()
print("字母顺序排列：")
print(place)
place.sort(reverse=True)
print("字母顺序反向排列：")
print(place)
print("您一共邀请了的人数：")
print(len(place))
